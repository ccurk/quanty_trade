func (m *MockExchange) SubscribeCandles(symbol string, callback func(Candle)) (func(), error) {
	// Simulate data every 1 second
	stop := make(chan struct{})
	go func() {
		price := 60000.0
		for {
			select {
			case <-stop:
				return
			default:
			}
			price += float64((time.Now().UnixNano() % 100) - 50)
			callback(Candle{
				Timestamp: time.Now(),
				Open:      price,
				High:      price + 10,
				Low:       price - 10,
				Close:     price,
				Volume:    1.0,
			})
			time.Sleep(1 * time.Second)
		}
	}()
	return func() {
		select {
		case <-stop:
		default:
			close(stop)
		}
	}, nil
}

